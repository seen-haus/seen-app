const invalidUrl = 'Website url is not valid';
export const isValidHttpUrl = (string, optional = true) => {
    let url;
		if (!string) return true;
    try {
        url = new URL(string);
    } catch (_) {
        console.log(invalidUrl);
        return invalidUrl;
    }

    return (url.protocol === "http:" || url.protocol === "https:") ? true : invalidUrl;
}

