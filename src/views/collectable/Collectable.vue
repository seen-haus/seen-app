<script>
import {computed, reactive, ref, onUnmounted} from "vue";
import {useRoute, useRouter} from "vue-router";
import {useMeta} from "vue-meta";
import {useStore} from "vuex";

import FencedTitle from "@/components/FencedTitle.vue";
import UnfencedTitle from "@/components/UnfencedTitle.vue";
import UserBadge from "@/components/PillsAndTags/UserBadge.vue";
import LiveIndicator from "@/components/PillsAndTags/LiveIndicator.vue";
import Tag from "@/components/PillsAndTags/Tag.vue";
import Container from "@/components/Container.vue";
import ArtistCard from "@/components/ArtistCard.vue";
import UserCard from "@/components/UserCard.vue";
import BidCard from "@/components/BidCard.vue";
import ListOfBuyers from "@/components/Lists/ListOfBuyers.vue";
import HeroGallery from "@/components/Media/HeroGallery.vue";
import {CollectablesService} from "@/services/apiService";
import {useToast} from "primevue/usetoast";

import useDarkMode from "@/hooks/useDarkMode";
import useCollectableInformation from "@/hooks/useCollectableInformation.js";
import {getEtherscanLink} from "@/services/utils";
import GLightbox from 'glightbox';
import 'glightbox/dist/css/glightbox.css';
import NftData from "@/views/collectable/components/NftData.vue";
import SocialSharing from "@/components/SocialSharing";

import useWeb3 from "@/connectors/hooks";

export default {
  name: "Collectable",
  components: {
    SocialSharing,
    FencedTitle,
    Container,
    UserBadge,
    Tag,
    LiveIndicator,
    ArtistCard,
    UserCard,
    BidCard,
    ListOfBuyers,
    HeroGallery,
    NftData,
    UnfencedTitle,
  },
  methods: {
    getBackgroundImage(backgroundImage) {
      if(backgroundImage) {
        return require('../../assets/images/' + backgroundImage)
      }
    },
    getCollectionVideoCallback(collectionName) {
      if(collectionName === '420') {
        return require('../../assets/videos/420-collection-2022.mp4');
      }
    }
  },
  props: {
    slug: {
      type: String || Boolean,
      default: false
    },
    overrideMedia: {
      type: Array || Boolean,
      default: false
    }
  },
  setup(props) {
    const toast = useToast();
    const route = useRoute();
    const router = useRouter();
    const state = reactive({
      loading: true,
      contractAddress: null,
      collectable: {},
      buyersVisible: 3,
    });
    const store = useStore();

    const { chainId } = useWeb3();

    const { darkMode, setDarkMode } = useDarkMode();

    const {
      collectable,
      collectableState,
      price,
      priceUSD,
      items,
      itemsOf,
      progress,
      isCollectableActive,
      isUpcomming,
      isOpenEdition,
      isVRFSale,
      itemsBought,
      hasRequestedVRF,
      hasFulfilledVRF,
      hasCommittedVRF,
      // Static
      type,
      media,
      // firstMedia,
      gallerySortedMedia,
      artist,
      artistStatement,
      title,
      description,
      events,
      startsAt,
      minimumStartsAt,
      endsAt,
      liveStatus,
      is_sold_out,
      is_closed,
      edition,
      edition_of,
      isTangible,
      isNft,
      isAuction,
      version,
      nextBidPrice,
      user,
      // Methods
      updateProgress,
      setCollectable,
      updateCollectableState,
      claim,
      pillOverride,
      collectionName,
    } = useCollectableInformation();

    const backgroundImage = ref(false);
    const titleMonospace = ref(false);

    const useSlug = props.slug ? props.slug : route.params["slug"];

    if(route.path === '/drops/v2/secret') {
      router.push({
        name: "secret",
      })
    }

    const darkModeEnabled = [
      '0xmons-mork',
      'crossover',
      'virus',
      'enkindle',
      'eye-contact',
      'face-off',
      'nosferatus-mushroom-party',
      'blueberry-kush',
      'trashed-hoodie',
      'beautiful-day',
      'santa-marijuana',
      'purple-dos-mart',
    ].indexOf(useSlug) > -1;

    setTimeout(() => {
      setDarkMode(darkModeEnabled);
    }, 200)

    onUnmounted(() => {
      setDarkMode(false);
    })

    // TODO: Make this into a DB datasource unless V3 no longer uses this
    if (darkModeEnabled) {
      switch(useSlug) {
        case '0xmons-mork':
          backgroundImage.value = '0xmons-tile.png';
          titleMonospace.value = true;
          break;
      }
    }

    const currentEndsAt = computed(() => {
      return endsAt.value;
    });

    const currentStartsAt = computed(() => {
      return startsAt.value;
    });

    const currentMinimumStartsAt = computed(() => {
      return minimumStartsAt.value;
    });

    const keywords = computed(() => {
      let words = ["collectable", "drop", "seen", "seen.haus"];
      if (isTangible) words.push("tangible");
      if (isNft) words.push("nft");
      if (isAuction) words.push("auction");
      else words.push("sale");

      return words.join(" ");
    });
    const {meta} = useMeta({
      title: title.value || "Loading...",
      meta: [
        {
          name: "keywords",
          content: "",
        },
        {
          name: "description",
          content: "",
        }
      ],
    });

    const navigateToCollection = function (slug) {
      router.push({
          name: 'collection',
          params: {
            collectionName: slug
          }
      });
    }

    const isLoading = computed(() => state.loading);

    const showAdditionalInformation = computed(
        () => type.value === "tangible" || type.value === "tangible_nft"
    );

    const updateMeta = () => {
      meta.title = title.value || "Collectable";
      meta.meta[0].content = keywords.value;
      meta.meta[1].content = description.value.replace(
          /<\/?("[^"]*"|'[^']*'|[^>])*(>|$)/g,
          ""
      ).replace(/&nbsp;/g, ' ');
    };

    const viewOnEtherscan = () => {
      const address = state.collectable.contract_address;
      let url = getEtherscanLink(1, address, "address");
      window.open(url, '_blank').focus()
    }
    const viewOnOpenSea = () => {
      let nftAddress = collectable.value.nft_contract_address
      let nftTokenId = collectable.value.nft_token_id
      let url = `https://opensea.io/assets/${nftAddress}/${nftTokenId}`;
      // if(!NaN(chainId.value) && Number(chainId.value) > 0) {
      //   url = `https://testnets.opensea.io/assets/${nftAddress}/${nftTokenId}`;
      // }
      window.open(url, '_blank').focus()
    }

    (async function loadCollectable() {
      state.loading = true;
      const slug = useSlug;
      const {data} = await CollectablesService.show(slug);

      // data.events.reverse(); // Right order
      // state.buyers.list = data.events;
      state.loading = false;
      state.collectable = data;
      state.contractAddress = data.contract_address;

      setCollectable(data);
      updateMeta();
    })();

    const openModal = (type, url) => {
      const lightbox = GLightbox({
        touchNavigation: true,
        loop: true,
        autoplayVideos: true
      });
      if (type === 'video' || type.includes("mp4") || type.includes("video")) {
        lightbox.setElements([{
          'href': url,
          'type': 'video',
          'source': 'youtube', //vimeo, youtube or local
        }])
      } else {
        lightbox.setElements([{
          'href': url,
          'type': 'image', //vimeo, youtube or local
        }]);
      }
      lightbox.open();
    };



    return {
      isLoading,
      collectable,
      collectableState,
      collectionName,
      price,
      priceUSD,
      items,
      itemsOf,
      progress,
      isCollectableActive,
      isUpcomming,
      // Static
      type,
      media,
      gallerySortedMedia,
      // firstMedia,
      artist,
      artistStatement,
      title,
      description,
      events,
      currentStartsAt,
      currentEndsAt,
      currentMinimumStartsAt,
      liveStatus,
      is_sold_out,
      is_closed,
      edition,
      edition_of,
      isTangible,
      isNft,
      isAuction,
      nextBidPrice,
      isOpenEdition,
      itemsBought,
      isVRFSale,
      hasRequestedVRF,
      hasFulfilledVRF,
      hasCommittedVRF,
      backgroundImage,
      darkMode,
      titleMonospace,
      // Methods
      updateProgress,
      viewOnEtherscan,
      viewOnOpenSea,
      openModal,
      showAdditionalInformation,
      updateCollectableState,
      claim,
      pillOverride,
      version,
      navigateToCollection,
      user,
    };
  },
};
</script>


